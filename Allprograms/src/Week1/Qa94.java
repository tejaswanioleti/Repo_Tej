package Week1;

public class Qa94 {
    public static void main(String[] args) {
        // Declare and initialize a string array
        String[] names = {"Teja", "Manu", "Akhil", "Swetha"};

        // Print each value using a for-each loop
        System.out.println("Array of String values:");
        for (String name : names) {
            System.out.println(name);
        }
    }
}
