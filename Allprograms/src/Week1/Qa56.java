package Week1;

//Program to demonstrate shift operators
public class Qa56 {
 public static void main(String[] args) {
     int a = 8;  // Binary: 1000

     System.out.println("Original a: " + a);
     System.out.println("Left Shift (a << 1): " + (a << 1));   // 16
     System.out.println("Right Shift (a >> 1): " + (a >> 1));  // 4
 }
}

