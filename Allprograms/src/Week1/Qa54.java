package Week1;

//Program to demonstrate relational operators
public class Qa54  {
 public static void main(String[] args) {
     int a = 5, b = 10;

     System.out.println("a == b: " + (a == b));
     System.out.println("a != b: " + (a != b));
     System.out.println("a > b: " + (a > b));
     System.out.println("a < b: " + (a < b));
     System.out.println("a >= b: " + (a >= b));
     System.out.println("a <= b: " + (a <= b));
 }
}

